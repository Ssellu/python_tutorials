"""
    < 비교 연산자 >
    - 양 항의 대소 관계에 관여하는 연산기호
    - 결과값의 자료형은 bool형 (True 혹은 False)
    <    : a < b
    <=   : a <= b
    >    : a > b
    >=   : a >= b
    ==   : a == b
    !=   : a != b

"""
num1 = 10
num2 = 3

print(num1 == num2)  # False
print(num1 != num2)  # True
print((num1 + num2) == 13)  # True
