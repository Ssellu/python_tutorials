"""
    < 산술 연산자 >
    - 사칙 연산에 관여하는 연산기호

    +   : a + b
    -   : a - b
    *   : a * b
    **  : a ** b  (제승 연산)
    /   : a / b
    //  : a // b  (몫)
    %   : a % b   (나머지)
"""
num1 = 10
num2 = 3

res = num1 + num2
print(res)  # 13

res = num1 - num2
print(res)  # 7

res = num1 * num2
print(res)  # 30

res = num1 ** num2
print(res)  # 1000

res = num1 / num2
print(res)  # 3.3333333333333335

res = num1 // num2
print(res)  # 3

res = num1 % num2
print(res)  # 1
