"""
     3. 논리형 - bool 형
        True, False 가 있다. 말 그대로 '참'과 '거짓'의 의미이다.

"""

a = True
print(a)        # True
print(type(a))  # <class 'bool'>

b = False
print(b)        # False
print(type(b))  # <class 'bool'>

# 주의! "True"나 "False" 는 bool 이 아니다.
a = "True"
print(a)        # True
print(type(a))  # <class 'str'>

